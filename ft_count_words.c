#include "libft.h"

int		ft_count_words(char const *s, char c)
{
	int i;
	int words;

	words = 0;
	i = 0;
	while (s[i] != '\0')
	{
		while (s[i] == c)
			i++;
		while (s[i] != c && s[i] != '\0')
			i++;
		if (s[i - 1] != c)
			words++;
	}
	return (words);
}
